function Cure:CreateChatbox()
	Chatbox = vgui.Create("deathrun.vgui.chatbox")
	Chatbox:AddTab("General chat", "icon16/keyboard.png")
	Chatbox:AddTab("Admin chat", "icon16/shield.png")
	Chatbox.Think = function()
		for k, v in pairs(MessageHolder.Data) do
			if (Chatbox:IsVisible()) then
				v.Alpha = 255
			end
		end
	end

	Chatbox:MakePopup()

	return Chatbox
end

function Cure:PlayerBindPress( ply, bind, pressed )
    if pressed and string.find( bind, "messagemode" ) then
    	if (Chatbox) then
    		if not (type(Chatbox) == "table") then
    			Chatbox:Remove()
    		end 
    	end

        Chatbox = Cure:CreateChatbox()

        Chatbox.TextEntry:SetText("")
        Chatbox.TextEntry:RequestFocus()

        MessageHolder:SetParent(Chatbox)
        return true
    end
end

