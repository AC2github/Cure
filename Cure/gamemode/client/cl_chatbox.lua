-- Add a message
function Cure.AddChatMessage(player, message, icon)
	-- Wait till chatbox is valid
	if not (Chatbox) or not (Chatbox:IsValid()) then return end

	local pnl = (#MessageHolder.Data + 1)

	-- Create
	MessageHolder.Data[pnl] = vgui.Create("deathrun.vgui.chatmessage")
	-- Message
	MessageHolder.Data[pnl]:SetText(message)
	-- Player
	MessageHolder.Data[pnl]:SetPlayer(player)

	-- Icon
	if (icon) then
		MessageHolder.Data[pnl]:SetIcon(icon)
	end

	-- Pos
	if (pnl == 1) then
		MessageHolder.Data[pnl]:SetPos(0, 0)
	else
		MessageHolder.Data[pnl-1]:PerformLayout()
		
		local x, y = MessageHolder.Data[pnl-1]:GetPos()
		local w, h = MessageHolder.Data[pnl-1]:GetSize()

		MessageHolder.Data[pnl]:SetPos(0, y + h + 1)
	end

	-- Add
	MessageHolder:Add(MessageHolder.Data[pnl])

	-- Scroll
	MessageHolder:ScrollToChild(MessageHolder.Data[pnl])

	-- Console
	MsgN((player and player:Name()..": " or "")..message)
end

function chat.AddText(...)
	local args = {...}
	local str = ""
	local usingcolor = false

	for k, v in pairs(args) do
		print(v)
		if (type(v) == "table") then
			str = str..Format("<color=%s, %s, %s>", v.r, v.g, v.b)
			usingcolor = true
		elseif (usingcolor) then
			if (type(v) == "string") then
				str = str..""..tostring(v).."</color>"
				usingcolor = false
			end
		else
			str = str..""..tostring(v)
		end
	end

	Cure.AddChatMessage(nil, str, Material("icon16/shield.png"))
end

function OnPlayerChat( ply, strText, bTeamOnly, bPlayerIsDead )
	-- Chatsounds
	if not ply.LastTrigger then
		ply.LastTrigger = 0
	end

	-- Console valid stuff
	if not (ply:IsValid()) then
		Cure.AddChatMessage(nil, "Console: "..strText, Material("icon16/application_xp_terminal.png"))
	else
		Cure.AddChatMessage(ply, strText)
	end

	-- Sounds
	for k, v in pairs( chatsounds ) do
		if (v.trigger == string.lower(strText) and ply:IsValid() and ply.LastTrigger < CurTime()) then
			surface.PlaySound(v.sound[1])
			ply.LastTrigger = CurTime() + 3
		end
	end
	
	-- Idk
	return true
end
hook.Add( "OnPlayerChat", "ChatBox.HandlePlayerChat", OnPlayerChat )

